#!/usr/bin/env node
// Bundles every .py file in scripts/ into one generated TS module so the
// website displays the exact same source the CLI scripts run — one source
// of truth (the .py files), no hand-copied duplicate.

import { readdirSync, readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";

const scriptsDir = path.dirname(fileURLToPath(import.meta.url));
const outFile = path.join(scriptsDir, "..", "frontend", "src", "lib", "methodCode.generated.ts");

const entries = readdirSync(scriptsDir)
  .filter((f) => f.endsWith(".py"))
  .sort()
  .map((f) => {
    const id = f.replace(/\.py$/, "");
    const code = readFileSync(path.join(scriptsDir, f), "utf8");
    return [id, code];
  });

const body = entries
  .map(([id, code]) => `  ${JSON.stringify(id)}: ${JSON.stringify(code)},`)
  .join("\n");

const output = `// AUTO-GENERATED by scripts/generate-method-code.mjs — do not edit by hand.
// Source of truth is the .py files in /scripts. Regenerated automatically
// before dev/build (see frontend/package.json), or run the generator directly.

export const methodCode: Record<string, string> = {
${body}
};
`;

mkdirSync(path.dirname(outFile), { recursive: true });
writeFileSync(outFile, output);
console.log(`Generated methodCode.generated.ts with ${entries.length} script(s).`);
